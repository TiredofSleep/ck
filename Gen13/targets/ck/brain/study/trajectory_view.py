"""
trajectory_view.py -- read cortex_history.jsonl + surprisal_log.jsonl
                     and render CK's learning trajectory as a markdown
                     summary with ASCII sparkline graphs.

Shows:
  - W_trace over time
  - Phi-proxy over time (computed from each snapshot's couplings)
  - emergent over time
  - top-coupling stability (which pair was top at each snapshot)
  - mean surprisal per logged window

Usage:
    python trajectory_view.py
    python trajectory_view.py --markdown > Gen13/targets/ck/brain/TRAJECTORY.md
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).parent.resolve()
GEN13_BRAIN = SCRIPT_DIR.parent
HISTORY = GEN13_BRAIN / "cortex_history.jsonl"
SURPRISAL = GEN13_BRAIN / "surprisal_log.jsonl"


def load_jsonl(path):
    items = []
    if not path.exists():
        return items
    with open(path) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                items.append(json.loads(line))
            except Exception:
                continue
    return items


def sparkline(values, width=40, lo=None, hi=None):
    """ASCII sparkline of `values`, scaled to `width` columns."""
    if not values:
        return ""
    chars = " .:-=+*#%@"
    if lo is None:
        lo = min(values)
    if hi is None:
        hi = max(values)
    if hi <= lo:
        hi = lo + 1e-9
    out = []
    # Pick `width` evenly-spaced indices
    n = len(values)
    if n <= width:
        sample = values
    else:
        idxs = [int(i * (n - 1) / (width - 1)) for i in range(width)]
        sample = [values[i] for i in idxs]
    for v in sample:
        frac = (v - lo) / (hi - lo)
        ci = max(0, min(len(chars) - 1, int(frac * (len(chars) - 1))))
        out.append(chars[ci])
    return "".join(out)


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--markdown", action="store_true",
                   help="Emit markdown (vs plain text)")
    p.add_argument("--history", default=str(HISTORY))
    p.add_argument("--surprisal", default=str(SURPRISAL))
    args = p.parse_args()

    history = load_jsonl(Path(args.history))
    surprisal = load_jsonl(Path(args.surprisal))

    if args.markdown:
        print("# CK trajectory")
        print()
        print(f"_generated by `Gen13/targets/ck/brain/study/trajectory_view.py`_")
        print(f"_history file: `{args.history}` ({len(history)} snapshots)_")
        print(f"_surprisal file: `{args.surprisal}` ({len(surprisal)} entries)_")
        print()
    else:
        print("=" * 78)
        print("CK trajectory view")
        print("=" * 78)

    if not history:
        print("(no history yet -- run cortex_backup.py to record snapshots)")
        return 0

    # Series
    ticks = [h.get('tick') for h in history]
    W_traces = [h.get('W_trace', 0) for h in history]
    emergents = [h.get('emergent', 0) for h in history]

    # Phi-proxy from top_couplings: total - min cut (approximation)
    phi_proxies = []
    for h in history:
        tc = h.get('top_couplings', [])
        if not tc:
            phi_proxies.append(0.0)
            continue
        # Total = sum of |W| over reported pairs (incomplete; approximation)
        total = sum(abs(c.get('W', 0)) for c in tc)
        # Min "cut" = the weakest of the top 5 (proxy)
        weakest = min(abs(c.get('W', 0)) for c in tc) if tc else 0
        phi_proxies.append(round(total - weakest, 4))

    # Notes
    notes = [h.get('note', '') for h in history]

    if args.markdown:
        print("## Snapshots\n")
        print(f"| # | tick | W_trace | emergent | top-coupling | phi_proxy_top5 | note |")
        print(f"|---|---|---|---|---|---|---|")
        for i, h in enumerate(history):
            tc = h.get('top_couplings', [])
            top = tc[0]['pair'] if tc else "-"
            top_W = tc[0]['W'] if tc else 0
            print(f"| {i+1} | {h.get('tick')} | {h.get('W_trace'):.4f} | "
                  f"{h.get('emergent'):.4f} | {top} (W={top_W:.4f}) | "
                  f"{phi_proxies[i]:.4f} | {h.get('note', '')[:60]} |")
        print()

        # Sparklines
        print("## Trends\n")
        if len(history) >= 2:
            print(f"```")
            print(f"W_trace   : [{sparkline(W_traces)}]  range [{min(W_traces):.4f}, {max(W_traces):.4f}]")
            print(f"emergent  : [{sparkline(emergents)}]  range [{min(emergents):.4f}, {max(emergents):.4f}]")
            print(f"phi (top5): [{sparkline(phi_proxies)}]  range [{min(phi_proxies):.4f}, {max(phi_proxies):.4f}]")
            print(f"```")
        else:
            print("(need >=2 snapshots for trend)")
        print()
    else:
        print(f"\n  {len(history)} snapshots")
        for i, h in enumerate(history):
            tc = h.get('top_couplings', [])
            top = tc[0]['pair'] if tc else "-"
            top_W = tc[0]['W'] if tc else 0
            note_short = (h.get('note', '') or '')[:50]
            print(f"  [{i+1}] tick={h.get('tick')} W_trace={h.get('W_trace'):.4f} "
                  f"emergent={h.get('emergent'):.4f} top={top}@{top_W:.3f} note={note_short}")
        if len(history) >= 2:
            print()
            print(f"  W_trace   : {sparkline(W_traces)}  [{min(W_traces):.4f}, {max(W_traces):.4f}]")
            print(f"  emergent  : {sparkline(emergents)}  [{min(emergents):.4f}, {max(emergents):.4f}]")

    # Surprisal trend
    if surprisal:
        wins = [s for s in surprisal if s.get('_event') == 'window']
        if wins:
            means = [w.get('mean_surprisal_window', 0) for w in wins]
            if args.markdown:
                print("## Surprisal\n")
                print(f"```")
                print(f"surprisal: [{sparkline(means)}]  range [{min(means):.4f}, {max(means):.4f}]")
                print(f"```")
                print()
                if len(means) >= 2:
                    delta = means[0] - means[-1]
                    direction = "DECREASING" if delta > 0 else ("INCREASING" if delta < 0 else "FLAT")
                    print(f"**Trend**: surprisal {direction} by {abs(delta):.4f} over the run.")
                    if delta > 0:
                        print(f"  -> consistent with predictive-coding / FEP (Bridge 5).")
            else:
                print(f"\n  Surprisal trend ({len(means)} windows):")
                print(f"    [{sparkline(means)}]  [{min(means):.4f}, {max(means):.4f}]")
                if len(means) >= 2:
                    delta = means[0] - means[-1]
                    print(f"    delta={delta:+.4f} ({'learning' if delta > 0 else 'not-learning'})")

    return 0


if __name__ == "__main__":
    sys.exit(main())
