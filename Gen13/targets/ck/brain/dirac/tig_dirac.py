def tensor_partition(n: int) -> list[tuple[str, ...]]:
    """The 2^n fine-tensor cells at level V^⊗n.  Each cell labeled by a sign string in {+,-}^n.
    All cells are pairwise orthogonal idempotents summing to e_0^⊗n."""
    cells = []
    for bits in range(2**n):
        sign_tuple = tuple('+' if (bits >> (n-1-k)) & 1 else '-' for k in range(n))
        cells.append(sign_tuple)
    return cells


def cell_to_tensor(cell: tuple[str, ...]) -> tuple[np.ndarray, ...]:
    """Convert a sign-tuple cell to an actual pure-tensor representation."""
    return tuple(P_PLUS if s == '+' else P_MINUS for s in cell)


def cells_orthogonal(cell_a: tuple[str, ...], cell_b: tuple[str, ...]) -> bool:
    """Two cells are orthogonal iff their sign tuples differ in at least one position
    (since p_+ · p_- = 0)."""
    return cell_a != cell_b


def cell_binomial_distribution(n: int) -> dict[int, int]:
    """For V^⊗n, count cells by number of '+' signs.
    Equals binomial coefficients C(n, k).  Sum = 2^n."""
    cells = tensor_partition(n)
    return dict(Counter(cell.count('+') for cell in cells))


