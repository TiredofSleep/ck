def s_n_orbits(n: int) -> list[set[tuple[str, ...]]]:
    """Orbits of S_n acting on the 2^n sign-cells of V^⊗n by slot permutation."""
    import itertools
    cells = [tuple('+' if (b >> (n-1-i)) & 1 else '-' for i in range(n)) for b in range(2**n)]
    perms = list(itertools.permutations(range(n)))
    
    orbits = []
    visited = set()
    for c in cells:
        if c in visited: continue
        orbit = set()
        for perm in perms:
            orbit.add(tuple(c[perm[i]] for i in range(n)))
        visited |= orbit
        orbits.append(orbit)
    return orbits


def schur_weyl_match(n: int) -> dict:
    """Return the SU(n) × U(1) decomposition pattern from S_n on V^⊗n."""
    orbits = s_n_orbits(n)
    sizes = sorted([len(o) for o in orbits], reverse=True)
    
    # Match to known SU(n) decompositions
    interpretations = {
        2: ("SU(2) × U(1)", "1 + 2 + 1 = 4 (electroweak doublet + 2 singlets)"),
        3: ("SU(3) × U(1)", "1 + 3 + 3̄ + 1 = 8 (Furey's Cl(6) decomposition)"),
        4: ("SU(4) × U(1)", "1 + 4 + 6 + 4 + 1 = 16 (Pati-Salam-like)"),
        5: ("SU(5) × U(1)", "1 + 5 + 10 + 10 + 5 + 1 = 32 (SU(5) GUT 16 + 16)"),
    }
    
    name, reading = interpretations.get(n, (f"SU({n}) × U(1)", f"binomial 2^{n} decomposition"))
    
    return {
        'level': n,
        'orbit_sizes': sizes,
        'binomial_pattern': sizes,
        'sum': sum(sizes),
        'gauge_group': name,
        'interpretation': reading,
    }


