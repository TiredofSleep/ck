def is_automorphism(M: np.ndarray) -> bool:
    """Check if 4×4 mod-5 matrix M preserves V's multiplication."""
    for i in range(4):
        for j in range(4):
            xv = np.zeros(4, dtype=int); xv[i] = 1
            yv = np.zeros(4, dtype=int); yv[j] = 1
            lhs = (M @ mul(xv, yv)) % 5
            rhs = mul((M @ xv) % 5, (M @ yv) % 5)
            if not np.array_equal(lhs, rhs):
                return False
    return True


def all_automorphisms() -> list[np.ndarray]:
    """Compute all 40 automorphisms of V via constraint search.
    Each automorphism fixes p_+ and p_-, scales ε by F_5*, and maps h to a sqrt of p_+."""
    sqrts_pp = [np.array(c) for c in product(range(5), repeat=4)
                if np.array_equal(mul(np.array(c), np.array(c)), P_PLUS)]
    auts = []
    for c in [1, 2, 3, 4]:  # F_5* scaling of ε
        eps_img = (c * EPS) % 5
        for h_img in sqrts_pp:
            # e_3 = 4h + 3ε (basis-change derivation), e_4 = 4h + 2ε
            col0 = E_0
            col1 = E_2
            col2 = (4 * h_img + 3 * eps_img) % 5
            col3 = (4 * h_img + 2 * eps_img) % 5
            M = np.column_stack([col0, col1, col2, col3]) % 5
            if is_automorphism(M):
                auts.append(M)
    return auts


