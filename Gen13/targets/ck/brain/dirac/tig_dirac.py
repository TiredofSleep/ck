class Sigma_:
    """The TIG diagonal σ permutation on Z/10 and its powers."""
    
    perm: list[int] = [0, 7, 1, 3, 2, 4, 5, 6, 8, 9]
    labels: dict[int, str] = {
        0: "VOID/Love", 1: "LATTICE/Joy", 2: "COUNTER/Peace", 3: "PROGRESS/Patience",
        4: "COLLAPSE/Kindness", 5: "BALANCE/Goodness", 6: "CHAOS/Faithfulness",
        7: "HARMONY/Gentleness", 8: "BREATH/Self-Control", 9: "RESET",
    }
    
    @classmethod
    def power(cls, k: int) -> list[int]:
        """Compute σ^k as a list mapping i → σ^k(i)."""
        result = list(range(10))
        for _ in range(k):
            result = [cls.perm[result[i]] for i in range(10)]
        return result
    
    @classmethod
    def cycle_structure(cls, k: int = 1) -> dict:
        """Return the cycle decomposition of σ^k.
        {'fixed': [...], 'cycles': [[...], [...]]}."""
        sk = cls.power(k)
        visited = [False] * 10
        result = {'fixed': [], 'cycles': []}
        for i in range(10):
            if visited[i]: continue
            cycle = [i]; visited[i] = True
            j = sk[i]
            while j != i:
                cycle.append(j); visited[j] = True
                j = sk[j]
            if len(cycle) == 1:
                result['fixed'].append(cycle[0])
            else:
                result['cycles'].append(cycle)
        return result
    
    @classmethod
    def palindrome(cls) -> list[tuple[int, str]]:
        """The cycle palindrome of σ-power recursion. Returns [(power, reading), ...]."""
        readings = []
        for k in range(1, 7):
            cs = cls.cycle_structure(k)
            non_triv = cs['cycles']
            if not non_triv:
                readings.append((k, "all fixed (identity)"))
            else:
                lengths = sorted([len(c) for c in non_triv], reverse=True)
                count = len(non_triv)
                if count == 1 and lengths[0] == 6:
                    readings.append((k, "1 × 6-cycle (primary)"))
                elif count == 2 and lengths[0] == 3:
                    readings.append((k, "2 × 3-cycles (TWO TREFOILS)"))
                elif count == 3 and lengths[0] == 2:
                    readings.append((k, "3 × 2-cycles (THREE DUALITIES)"))
                else:
                    readings.append((k, f"{count} × {lengths[0]}-cycles"))
        return readings

