class V_:
    """Class-style facade for the algebra V."""
    
    @staticmethod
    def dim() -> int:
        return 4
    
    @staticmethod
    def field_size() -> int:
        return 5
    
    @staticmethod
    def num_elements() -> int:
        return 625  # 5^4
    
    @staticmethod
    def idempotents() -> list[tuple[int, ...]]:
        """All x ∈ V with x² = x. Returns 4 idempotents (incl. 0)."""
        return [tuple(x) for x in all_vectors() if np.array_equal(mul(x, x), x)]
    
    @staticmethod
    def annihilator() -> list[tuple[int, ...]]:
        """Non-zero left annihilator: {x : x·y = 0 for all y}."""
        result = []
        for x in all_vectors():
            if np.all(x == 0): continue
            if all(np.all(mul(x, y) == 0) for y in all_vectors()):
                result.append(tuple(x))
        return result
    
    @staticmethod
    def is_commutative() -> bool:
        """Verify V is commutative."""
        for x in all_vectors():
            for y in all_vectors():
                if not np.array_equal(mul(x, y), mul(y, x)):
                    return False
        return True
    
    @staticmethod
    def is_associative_on_basis() -> bool:
        """Test associativity on basis-vector triples."""
        basis = [E_0, E_2, E_3, E_4]
        for x in basis:
            for y in basis:
                for z in basis:
                    if not np.array_equal(mul(mul(x, y), z), mul(x, mul(y, z))):
                        return False
        return True
    
    @staticmethod
    def is_power_associative() -> bool:
        """Verify (xx)x = x(xx) for all x."""
        for x in all_vectors():
            xx = mul(x, x)
            if not np.array_equal(mul(xx, x), mul(x, xx)):
                return False
        return True
    
    @staticmethod
    def associator_image() -> set[tuple[int, ...]]:
        """Compute the image of the associator [x, y, z] = (xy)z - x(yz)."""
        image = set()
        # Sample (full enumeration is 625^3 = too large)
        basis = [E_0, E_2, E_3, E_4]
        for x in basis:
            for y in basis:
                for z in basis:
                    a = (mul(mul(x, y), z) - mul(x, mul(y, z))) % 5
                    image.add(tuple(a))
        return image
    
    @staticmethod
    def weight(x: np.ndarray) -> int:
        """The weight homomorphism ω: V → F_5.
        Defined by ω(p_+) = 1, ω(p_-) = 0, ω(ε) = 0, ω(h) = -1.
        In old basis (e_0, e_2, e_3, e_4): ω = α - δ where x = α p_+ + β p_- + γ ε + δ h."""
        a, b, c, d = int(x[0]), int(x[1]), int(x[2]), int(x[3])
        alpha = (a + b) % 5
        delta = (4 * (c + d)) % 5  # 1/4 = 4 mod 5? No, 4 = -1, 1/4 = -1 = 4 mod 5? Let me check.
        # 4 × 4 = 16 = 1 mod 5, so 1/4 = 4. Yes.
        # h = 2(e_3 + e_4), so δ = (c+d)/(2*2) = (c+d)/4 = 4(c+d) mod 5
        return (alpha - delta) % 5

