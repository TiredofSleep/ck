def is_cover(S, edges):
    return all(len(set(S) & set(e)) > 0 for e in edges)

def all_vertex_covers(vertices, edges):
    covers = []
    for size in range(1, len(vertices)+1):
        for S in combinations(vertices, size):
            if is_cover(set(S), edges):
                covers.append(set(S))
    # Filter to minimal covers
    minimal = []
    for c in covers:
        is_min = True
        for c2 in covers:
            if c2 != c and c2 < c:
                is_min = False
                break
        if is_min:
            minimal.append(sorted(c))
    return minimal

