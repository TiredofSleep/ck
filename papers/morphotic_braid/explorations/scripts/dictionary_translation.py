# ============================================================
# 8. ALTERNATING GROUP STRUCTURE / DERIVATIONS
# ============================================================
print("\n8. AUTOMORPHISMS AND DERIVATIONS")
def is_aut(perm, T):
    for i in range(N):
        for j in range(N):
            if perm[T[i][j]] != T[perm[i]][perm[j]]: return False
    return True

# All automorphisms fixing 0 and 7
auts = []
for p in permutations(range(N)):
    if p[0]==0 and p[7]==7 and is_aut(p, TSML):
        auts.append(p)
print(f"   |Aut(TSML) fixing 0 and 7| = {len(auts)}")
# Describe
for a in auts:
    moved = [(i, a[i]) for i in range(N) if a[i] != i]
    if not moved: print(f"     identity")
    else: print(f"     moves: {moved}")

